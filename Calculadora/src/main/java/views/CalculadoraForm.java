    private void backBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBotonActionPerformed
        String x = getScreenText();
        if(!x.equals("0") && x.length() >= 1){
            if(x.length() == 1){
                setTextToScreen("0");
            }
            else{
                setTextToScreen(x.substring(0,x.length()-1));
            }
        }
    }//GEN-LAST:event_backBotonActionPerformed
     private void printNumberActionPerformed(java.awt.event.ActionEvent evt) {                                            
        JButton b = (JButton)evt.getSource();
        if (freeToStart){
            clear();
            setTextToScreen(b.getText());            
        }
        else
        {
            if(!getScreenText().equals("0")){
                setTextToScreen(getScreenText()+ b.getText());
            }
            else
            {
                setTextToScreen(b.getText());
            }            
        }
    }  
